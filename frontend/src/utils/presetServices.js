// Helper to generate the data URI of the SVG preset
export const getPresetDataUri = (preset) => {
  const svg = `<svg viewBox="0 0 100 100" xmlns="http://www.w3.org/2000/svg">
    <defs>
      <linearGradient id="grad_${preset.name.replace(/[^a-zA-Z]/g, '_')}" x1="0%" y1="0%" x2="100%" y2="100%">
        <stop offset="0%" stop-color="${preset.color1}"/>
        <stop offset="100%" stop-color="${preset.color2}"/>
      </linearGradient>
      <filter id="dropShadow" x="-20%" y="-20%" width="140%" height="140%">
        <feDropShadow dx="0" dy="3" stdDeviation="2.5" flood-color="#000000" flood-opacity="0.25"/>
      </filter>
    </defs>
    <rect width="100" height="100" rx="22" fill="url(#grad_${preset.name.replace(/[^a-zA-Z]/g, '_')})"/>
    <!-- Subtle top gloss accent -->
    <path d="M0 0 L100 0 L100 40 Q50 60 0 40 Z" fill="white" fill-opacity="0.12"/>
    <g filter="url(#dropShadow)">
      ${preset.iconPath}
    </g>
  </svg>`;
  return `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`;
};
